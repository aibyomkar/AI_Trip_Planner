import streamlit as st
import requests
import datetime
import sys

BASE_URL = 'http://localhost:8000' # backend end point

st.set_page_config(
    page_title="AI Trip Planner",
    page_icon=":airplane_departure:",
    layout="wide",
    initial_sidebar_state = 'expanded'
)

st.title = 'AI Trip Planner'

# inititalize chat history
if 'messages' not in st.session_state:
    st.session_state.messages = []

# display chat history
st.header('How can I help you in planning a trip? Let me know where you want to visit.')

# chat input box at the bottom
with st.form(key = 'query_form', clear_on_submit = True):

    user_input = st.text_input('User Input', placeholder = 'e.g. Plan a trip to Goa for 5 days')
    submit_button = st.form_submit_button('Send')

if submit_button and user_input.strip():
    try:
        # # Show user message
        # Show thinking spinner while backend processes

        with st.spinner('A.I. is thinking...'):
            payload = {'question': user_input}
            response = requests.post(f'{BASE_URL}/query', json = payload)

        if response.status_code == 200:
            answer = response.json().get('answer', 'No answer returned.')
            markdown_content = f'''
            # AI Trip Plan
            
            # **Generated:** {datetime.datetime.now().strftime('%Y-%m-%d at %H:%M')}
            # **Created by:** Omkar\'s AI Trip Planner
             
            ---
            
            {answer}
             
            ---
             
            *This trip plan was generated by AI. Please verify all information, especially prices, operating hours, and trip requirements before your trip.*
            '''

            st.markdown(markdown_content)

        else:
            st.error(' AI failed to respond: ' + response.text)

    except Exception as e:
        raise f'The response failed due to: {e}'